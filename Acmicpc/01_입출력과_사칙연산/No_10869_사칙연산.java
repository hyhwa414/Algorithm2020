import java.util.Scanner;

public class No_10869_사칙연산 {
	public static void main(String[] args) {
		Scanner S = new Scanner(System.in);
		
		int A = S.nextInt();
		int B = S.nextInt();
		
		System.out.println(A + B);
		System.out.println(A - B);
		System.out.println(A * B);
		System.out.println(A / B);
		System.out.println(A % B);
	}
}
